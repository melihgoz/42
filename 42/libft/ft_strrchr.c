#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	while (*s != '\0')
	{
		if (c == *s)
		{
			return ((char *)s);
		}
		s++;
	}
	if (*s == 0 || (char)c == 0)
	{
		return (c);
	}
	return (NULL);
}